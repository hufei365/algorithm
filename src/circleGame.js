/**
 * @param {number[][]} toys
 * @param {number[][]} circles
 * @param {number} r
 * @return {number}
 */
var circleGame = function (toys, circles, r) {

    let count = 0;
    for(let i = 0; i < toys.length; i++){
        if(toys[2] > r) continue
        for(let j = 0; j < circles.length; j++){
            if( Math.abs(toys[i][0] - circles[j][0]) > r ) continue
            if( Math.abs(toys[i][1] - circles[j][1]) > r ) continue
            if (isContain(toys[i], [...circles[j], r])) {
                count++
                break;
            }
        }
    }
    return count

    function isContain(circle1, circle2) {
        const [x1, y1, r1] = circle1, [x2, y2, r2] = circle2;
        if (r2 < r1) return false;

        const a = Math.abs(x1 - x2)
        const b = Math.abs(y1 - y2);
        const c = Math.sqrt(a * a + b * b)

        return Math.abs(r1 - r2) >= c
    }
};

